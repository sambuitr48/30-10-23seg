package Supplier;

public class Supplierx {
    private String name;
    private Integer age;
    private String skinColor;

    public Supplierx(String name, Integer age, String skinColor) {
        this.name = name;
        this.age = age;
        this.skinColor = skinColor;
    }

    public String getName() {
        return name;
    }

    public Integer getAge() {
        return age;
    }

    public String getSkinColor() {
        return skinColor;
    }
}
